from functools import lru_cache
import sys
# sys.setrecursionlimit(1000000)

a = int(input())
n = input().split()

@lru_cache(None)
def calculate(num):
    if num % 2 == 1:
        return 1

    return 1 + (calculate(num // 2) ** 2) % 1000000007


total = 1
for i in n:
    total = total * calculate(int(i)) % 1000000007

print(total)
